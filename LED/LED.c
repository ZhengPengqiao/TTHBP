#include "LED.h"

/*******************************************************************************
 * 函数名称 : setLedOn
 * 函数介绍 : 点亮指定小灯
 * 参数介绍 :
 * 返回值  :  无
 ******************************************************************************/
void setLedOn()
{
	LED = 0;
}
 
 /*******************************************************************************
 * 函数名称 : setLedOff
 * 函数介绍 : 熄灭指定小灯
 * 参数介绍 :
 * 返回值  :  无
 ******************************************************************************/
void setLedOff()
{
	LED = 1;
}
 
 
 /*******************************************************************************
 * 函数名称 : setLedToggle
 * 函数介绍 : 反转指定LED灯
 * 参数介绍 :
 * 返回值  :  无
 ******************************************************************************/
void setLedToggle()
{
	LED = !LED;
}